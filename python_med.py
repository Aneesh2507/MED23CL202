"""#List processing in python

Return a list were each element is a running sum.
sample input:[10,20,5,1]
sample output:[10,30,35,36]

#STEPS:-
1. create a empty result list.
2. keep a total variable starting at 0.
3. loopnthrough each number.
4. add to total,append total to result.
"""

def runningSum(nums):
  result=[]
  total=0
  for n in nums: #loop started
    total+= n    # add the value to total
    result.append(total) # append the sum to the result list
  return(result)
print(runningSum([10,20,30,2,-1]))

