# Grocery Store Checkout
# Available products
products = {
    "Apples": 50,
    "Bananas": 30,
    "Milk": 60,
    "Bread": 40,
    "Eggs": 70
}

# Empty cart
cart = {}

print("\n===== Grocery Store Menu =====")
print("1. Show available products and prices")
print("2. Empty cart")
print("3. Display menu")
print("4. Add item to cart")
print("5. Remove item from cart")
print("6. Show cart")
print("7. Exit")

while True:
    choice = input("\nEnter your choice (1-7): ")

    if choice == "1":
        print("\n--- Available Products ---")
        for item in products:
            print(item, ":", products[item])

    elif choice == "2":
        cart.clear()
        print("\nCart is now empty!")

    elif choice == "3":
        print("\n===== Grocery Store Menu =====")
        print("1. Show available products and prices")
        print("2. Empty cart")
        print("3. Display menu")
        print("4. Add item to cart")
        print("5. Remove item from cart")
        print("6. Show cart")
        print("7. Exit")

    elif choice == "4":
        print("\n--- Available Products ---")
        for item in products:
            print(item, ":", products[item])
        item = input("Enter product name to add: ")
        if item in products:
            qty = int(input("Enter quantity: "))
            if item in cart:
                cart[item] = cart[item] + qty
            else:
                cart[item] = qty
            print(qty, item, "added to cart.")
        else:
            print("Item not available.")

    elif choice == "5":
        if cart == {}:
            print("\nCart is empty. Nothing to remove.")
        else:
            print("Your Cart:", cart)
            item = input("Enter product name to remove: ")
            if item in cart:
                qty = int(input("Enter quantity to remove: "))
                if qty >= cart[item]:
                    del cart[item]
                    print(item, "removed from cart.")
                else:
                    cart[item] = cart[item] - qty
                    print(qty, item, "removed from cart.")
            else:
                print("Item not in cart.")

    elif choice == "6":
        if cart == {}:
            print("\nYour cart is empty.")
        else:
            print("\n--- Your Cart ---")
            total = 0
            for item in cart:
                qty = cart[item]
                price = products[item] * qty
                total = total + price
                print(item, "x", qty, "=", price)
            print("Total Bill:", total)

    elif choice == "7":
        print("Thank you for shopping! Goodbye.")
        break

    else:
        print("Invalid choice, try again.")